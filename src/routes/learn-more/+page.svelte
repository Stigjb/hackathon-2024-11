<h2>Learn more about food waste</h2>

<p>
There are plenty of good resources on the Internet about the causes of food waste,
how it's measured, and what we might do about it.
</p>

<h3>Did you know that:</h3>

<ul>
<li>More food is wasted in households than in the industry</li>
<li>The biggest category of household food waste is leftovers</li>
<li>
  The age group with the most food waste is 40-49
</li>
<li>
  Most household food waste is used to make biogas
  </li>
</ul>

